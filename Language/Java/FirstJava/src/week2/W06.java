package week2;

public class W06 {
    public static void main(String[] args) {
        int number1 = 10;
        number1 += 2;
        System.out.println(number1);

        number1 -= 2;
        System.out.println(number1);

        number1 *= 2;
        System.out.println(number1);

        number1 /= 2;
        System.out.println(number1);

        number1 %= 2;
        System.out.println(number1);
    }
}
